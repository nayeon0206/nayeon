// 정수 내림차순으로 배치하기

// 함수 solution은 정수 n을 매개변수로 입력받습니다.
// n의 각 자릿수를 큰것부터 작은 순으로 정렬한 새로운 정수를 리턴해주세요.
// 예를들어 n이 118372면 873211을 리턴하면 됩니다.

// n = 118372
// return = 873211

function solution(n) {
    // 1. 숫자를 문자열로 변환
    // 숫자를 자릿수로 분리하려면 n.toString(); 메서드를 써서 숫자를 문자열로 변환해야 함
    let str = n.toString();

    // 2. 문자열을 배열로 변환 후 내림차순 정렬
    // split('') 메서드는 문자열의 각 문자를 배열의 요소로 분리
    // sort() 메서드는 배열을 정렬 요소를 문자열로 비교, 숫자로 정렬하려면 비교함수를 제공해야 한다.
    // (a, b) => b - a -> b-a가 양수면 b가 a보다 먼저 온다. (내림차순 정렬)
    let sortedArray = str.split('').sort((a, b) => b - a);

    // 3. 정렬된 배열을 문자열로 합치기
    // join('') 메서드는 배열의 요소를 하나의 문자열로 합친다.
    let sortedStr = sortedArray.join('');

    // 4. 문자열을 정수로 변환하여 반환
    // parseInt() 함수는 문자열을 숫자로 변환
    return parseInt(sortedStr, 10);
}
