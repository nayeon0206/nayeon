// a<b인 경우는 a=3, b=5와 같은 경우이다. 그래서 범위의 시작점을 a로 잡아서 +1씩 해나가고 끝지점을 b로 잡는다.
// for(i=a; i<=b; i++)
// for문을 돌면서 i값을 sum에 더한다.
// sum += i
// i=b까지만 반복하고 범위를 넘어가면 for문을 빠져나온다.

// b<a 일 경우
