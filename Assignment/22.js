// for(i=a; i>=b; i--)
//     for문을 돌면서 i값을 sum에 더한다.
//     sum += i
//     i=b까지만 반복하고 범위를 넘어가면 for문을 빠져나온다.
//     여기까지하면 a===b인 경우를 제외하고는 sum에 값이 담겨있을 것이다.

//     이제 마지막으로 a===b인 경우를 처리하기 위해서 삼항연산자를 이용한다.
